    const user = await prisma.user.create({
      data: {
        email,
        name,
        passwordHash,
        salt,
        currentApostleId: 'peter', // –ü–æ —É–º–æ–ª—á–∞–Ω–∏—é –Ω–∞–∑–Ω–∞—á–∞–µ–º –ü–µ—Ç—Ä–∞
      },
      select: {
        id: true,
        email: true,
        name: true,
        currentApostleId: true,
        joinDate: true,
      }
    });

    console.log('‚úÖ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Å–æ–∑–¥–∞–Ω:', user.email);

    // –í–æ–∑–≤—Ä–∞—â–∞–µ–º –¥–∞–Ω–Ω—ã–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è (–±–µ–∑ –ø–∞—Ä–æ–ª—è)
